#include "common.h"

/*
*********************************************************************************************************
*	函 数 名: issafe
*	功能说明: 判断一个值是否在安全范围内
*	形    参:  无
*	返 回 值: 0:安全   -1:不安全
*********************************************************************************************************
*/
int issafe(int value, int min, int max)
{
    int val = (int)value;	 
	  if(val < min || val > max)
			return -1;
	  else
			return 0;
}

